    private void jBtAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAdicionarActionPerformed
        try {
            String nome = this.jTfNome.getText();
            
            double valor = Double.parseDouble(jTfValor.getText());
            
            Categoria categoria;
            
            switch (jCbCategoria.getSelectedItem().toString().toLowerCase()) {
                case "entrada":
                    categoria = Categoria.ENTRADA;
                    break;
                    
                case "principal":
                    categoria = Categoria.PRINCIPAL;
                    break;
                    
                case "sobremesa":
                    categoria = Categoria.SOBREMESA;
                    break;
                    
                default:
                    throw new IllegalArgumentException("categoria n√£o selecionada");
            }
            
            Prato prato = new Prato(valor, nome, categoria);
            pedido.incluirPrato(prato);
            
            jTfNome.setText("");
            jTfValor.setText("");
            jCbCategoria.setSelectedIndex(0);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Valor incorreto");
        } catch (IllegalArgumentException e) {        
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jBtAdicionarActionPerformed

